#Para imprimir algo en Python, se utiliza el función print
print("Hello Python") #En python se pueden usar las dobles comillas para un String
print('Hello Python') #En Python también se pueden usar las comillas simples para un string

#Ahora le vamos a decir a python que imprima el tipo de dato que ingresamos para imprimir y para ello usamos la función type dentro de print
print(type("Hello Python"))#Así que nos devuelte <class 'str'> indicandonos que es de clase string
print(type(12))#Así que nos devuelte <class 'int'> indicandonos que es de clase integer
print(type(12.3))#Así que nos devuelte <class 'float'> indicandonos que es de clase float
print(type(True))#Así que nos devuelte <class 'bool'> indicandonos que es de clase boolean
print(type(1+2j))#Así que nos devuelte <class 'complex'> indicandonos que es de clase complex (complejo)
